package day05_matematikselislemler;

public class Test01_PrePost {
    public static void main(String[] args) {

        int sayi = 10;

        System.out.println(++sayi);
        System.out.println("Pre Increment'den sonra sayi " + " " + sayi);

        System.out.println(sayi++);
        System.out.println("Post Increment'den sonra sayi" + " " + sayi);

        System.out.println(--sayi);


        System.out.println(sayi--);
        System.out.println(sayi);












































    }
}
